class Calculadora
	def add(num1,num2)
		puts "Suma -> #{num1} + #{num2} = #{num1+num2} " 
	end
	def substraction(num1,num2)
		puts "Resta -> #{num1} - #{num2} = #{num1-num2} " 
	end
	def multiplication(num1,num2)
		puts "Multiplicacion -> #{num1} * #{num2} = #{num1*num2} " 
	end
	def division(num1,num2)
		puts "Division -> #{num1} / #{num2} = #{num1/num2} " 
	end
end